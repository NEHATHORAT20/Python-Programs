#Write a program which accepts two numbers and prints addition, subtraction, multiplication and division.

def ASMD(No1 , No2):
    print("Addition is : " , No1 + No2)
    print("Subtraction is : " , No1 - No2)
    print("Multiplication is : " , No1 * No2)
    print("Division is : " , No1 / No2)

ASMD(12, 6)
